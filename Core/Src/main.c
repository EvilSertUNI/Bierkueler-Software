void Delay_us(uint16_t us) {
    __HAL_TIM_SET_COUNTER(&htim6, 0);
    while ((uint16_t)__HAL_TIM_GET_COUNTER(&htim6) < us);
}

uint8_t DS18B20_Start(void) {
    uint8_t Response = 0;
    HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_RESET);
    Delay_us(480);
    HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_SET);
    Delay_us(80);
    if (!(HAL_GPIO_ReadPin(DS_PORT, DS_PIN))) Response = 1;
    else Response = 0;
    Delay_us(400);
    return Response;
}

void DS18B20_Write(uint8_t data) {
    for (int i=0; i<8; i++) {
        if ((data & (1<<i)) != 0) {
            HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_RESET);
            Delay_us(1);
            HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_SET);
            Delay_us(60);
        } else {
            HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_RESET);
            Delay_us(60);
            HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_SET);
            Delay_us(1);
        }
    }
}

uint8_t DS18B20_Read(void) {
    uint8_t value = 0;
    for (int i=0; i<8; i++) {
        HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_RESET);
        Delay_us(2);
        HAL_GPIO_WritePin(DS_PORT, DS_PIN, GPIO_PIN_SET);
        Delay_us(12);
        if (HAL_GPIO_ReadPin(DS_PORT, DS_PIN)) {
            value |= (1<<i);
        }
        Delay_us(50);
    }
    return value;
}
